import math

a=math.pi
print("{:.2f}".format(a))

a=math.e
print("{:.2f}".format(a))

a=math.ceil(3.14)
print("{:.2f}".format(a))

a=math.floor(3.14)
print("{:.2f}".format(a))

a=math.factorial(4)#阶乘
print("{:.2f}".format(a))

a=math.sqrt(2)
print("{:.2f}".format(a))

a=math.degrees(math.pi)#返回角度值(弧度值变为角度值）
print("{:.0f}".format(a))

a=math.radians(180)#返回弧度值
print("{:.0f}".format(a))