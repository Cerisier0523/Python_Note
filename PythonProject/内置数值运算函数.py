import math
#绝对值
print(abs(-10.01))

#求余商 结果(x/y,x%y)
print(divmod(10,3))

#求幂 结果 (x**y)%z(z可省略)
print(pow(3,pow(10,3),10000))

#四舍五入 后者为保留小数位数，默认为0
print(round(3.14,2))

#返回最大值
print(max(3.14,2,5,64))

#返回最小值
print(min(3.14,2,5,64))

#强制类型转换int
print(int(3.14))

#强制类型转换float
print(float(3))

#转化为复数，增加虚数部分
print(complex(3))





